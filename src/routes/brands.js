import { Router } from 'express';
const router = Router();

import controller from '../controllers/brands.js';
import middleware from '../middlewares/middleware.js';

const { find, add, update, remove } = controller;

const { checkToken, checkUserIsActive } = middleware;

/**
 * @swagger
 * tags:
 *   name: Brands
 *   description: The API brands
 * /brands:
 *   get:
 *     summary: Lists all the books
 *     tags: [Brands]
 *     responses:
 *       200:
 *         description: The list of the books
 *         content:
 *           application/json:
 *             schema:
 *               type: array
 */

router.get('/', checkToken, checkUserIsActive, find);

/**
 * @swagger
 * tags:
 *   name: Brands
 *   description: The API brands
 * /brands:
 *   post:
 *     summary: Create a new book
 *     tags: [Brands]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             $ref: '#/components/schemas/Book'
 *     responses:
 *       200:
 *         description: The created book.
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Book'
 *       500:
 *         description: Some server error
 */

router.post('/', checkToken, checkUserIsActive, add);

